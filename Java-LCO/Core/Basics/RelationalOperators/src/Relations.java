public class Relations {

    public static void main(String[] args){

        int Number1 = 10;
        int Number2 = 25;

        System.out.println(Number1 == Number2);  //false
        System.out.println(Number1 != Number2);  //true
        System.out.println(Number1 > Number2);   //false
        System.out.println(Number1 < Number2);   //true


//        If any one or both is/are TRUE then then the result will come TRUE
//        If Both are FALSE then the result come false
        System.out.println(Number1 <= Number2);  //true
        System.out.println(Number1 >= Number2);  //false




    }
}