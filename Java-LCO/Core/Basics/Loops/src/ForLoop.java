public class ForLoop {

    public static void main(String[] args){

//        FOR LOOP (MOST USED/FAMOUS LOOP) - HAVE TO MASTER IN IT

//       FORMAT:
//
//        for (initializer; condition; increment/decrement){
//            System.out.println(variable name);
//        }


//        WORKING:
//        We have to PUT EVERYTHING INSIDE THE PARENTHESES after the 'for'
//        FIRST it do the INITIALIZATION (Just ONE TIME in complete process)
//        Then CHECKS the CONDITION
//        If it is TRUE then it PRINTS the VARIABLE
//        Then PERFORMS ARITHMETIC
//        Then PRINTS the RESULT
//        Does the SAME until the CONDITION BECOMES FALSE


        for (int mVariable = 1; mVariable < 10; mVariable++){
            System.out.println(mVariable);
        }

    }

}