public class WhileLoop {

    public static void main(String[] args){

        // WHILE LOOP


//        WORKING:
//        First it checks weather the is TRUE or FALSE
//        If it is TRUE then it JUMPS TO CODE BLOCKS
//        And then PRINT 'sout'
//        And then FOLLOW the ARITHMETIC
//        And If the RESULT OF ARITHMETIC is TRUE for the FIRST CONDITION then it AGAIN PRINTS the RESULT
//        It goes on CONTINUING TILL the CONDITION becomes FALSE


//        We can also USE SIGNS like =< or =>


//        This will print from 1-9
        int mVariable = 1;

        while (mVariable < 10){
            System.out.println(mVariable);
            mVariable++;
        }

        System.out.println("I am out of Loop");



    }

}