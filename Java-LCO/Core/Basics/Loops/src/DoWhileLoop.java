public class DoWhileLoop {


    public static void main(String[] args){

        // DO WHILE LOOP

        int mVariable = 11;

//        WORKING: OPPOSITE OF WHILE LOOP
//        FIRST it PRINTS what is INSIDE the CODE BLOCKS
//        THEN looks for the ARITHMETIC
//        THEN CHECKS WHETHER the CONDITION is TRUE or FALSE
//        And if it is TRUE then it PRINTS THE LOOP ACCORDING THE CONDITION
//        If FALSE then ONLY the FIRST sout is PRINTED

        do {
            System.out.println(mVariable);
            mVariable++;
        }while (mVariable < 10);






    }

}